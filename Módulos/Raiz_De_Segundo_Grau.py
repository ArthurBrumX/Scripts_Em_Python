from math import sqrt 
    # Importando a Biblioteca (math) Mas Somente a Fucionalidade (Sqrt) - Raiz Quadrada

num = int (input("Digite um numero: "))
    # Solicitando Ao Usuário um Valor Inteiro Para a Variavel (num)

raiz = sqrt(num)
    # Atribuindo a Variável (Raiz) A conversao do valor de (num) para (Sqrt) - Raiz Quadrada
    # Raiz = Conteudo de (num) convertido em (Sqrt) - Raiz Quadrada

print ("A raiz De {} é igual a  {}".format(num, raiz))
    # para mostrar o Numero Redondo Pra Cima (num, math.ceil(Raiz))
    # Imprimindo Na Tela o Resultado Da conversao de (num) Para Raiz Quadrada





